#include<stdio.h>
void main()
{
    int n1=2006;
    int n2=27;
    int sum=n1+n2;
    int sub=n1-n2;
    int prod=n1*n2;
    int quo=n1/n2;
    int rem=n1%n2;
    printf("addition:%d\n",sum);
    printf("subtraction:%d\n",sub);
    printf("multiplication:%d\n",prod);
    printf("division(Q):%d\n",quo);
    printf("division(R):%d\n",rem);
}